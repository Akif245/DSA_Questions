import java.util.Scanner;

class Day1{
          public static void main(String[] args) {
                    System.out.println("Natural Numbers  ");
                    Scanner scan = new Scanner(System.in);
                    int n = scan.nextInt();
                    for (int i = n; i < 10; i++) {
                            System.out.println(i+1);  
                    }
          }
}